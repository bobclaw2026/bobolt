import Foundation

public enum BoboltRemindersCommand: String, Codable, Sendable {
    case list = "reminders.list"
    case add = "reminders.add"
}

public enum BoboltReminderStatusFilter: String, Codable, Sendable {
    case incomplete
    case completed
    case all
}

public struct BoboltRemindersListParams: Codable, Sendable, Equatable {
    public var status: BoboltReminderStatusFilter?
    public var limit: Int?

    public init(status: BoboltReminderStatusFilter? = nil, limit: Int? = nil) {
        self.status = status
        self.limit = limit
    }
}

public struct BoboltRemindersAddParams: Codable, Sendable, Equatable {
    public var title: String
    public var dueISO: String?
    public var notes: String?
    public var listId: String?
    public var listName: String?

    public init(
        title: String,
        dueISO: String? = nil,
        notes: String? = nil,
        listId: String? = nil,
        listName: String? = nil)
    {
        self.title = title
        self.dueISO = dueISO
        self.notes = notes
        self.listId = listId
        self.listName = listName
    }
}

public struct BoboltReminderPayload: Codable, Sendable, Equatable {
    public var identifier: String
    public var title: String
    public var dueISO: String?
    public var completed: Bool
    public var listName: String?

    public init(
        identifier: String,
        title: String,
        dueISO: String? = nil,
        completed: Bool,
        listName: String? = nil)
    {
        self.identifier = identifier
        self.title = title
        self.dueISO = dueISO
        self.completed = completed
        self.listName = listName
    }
}

public struct BoboltRemindersListPayload: Codable, Sendable, Equatable {
    public var reminders: [BoboltReminderPayload]

    public init(reminders: [BoboltReminderPayload]) {
        self.reminders = reminders
    }
}

public struct BoboltRemindersAddPayload: Codable, Sendable, Equatable {
    public var reminder: BoboltReminderPayload

    public init(reminder: BoboltReminderPayload) {
        self.reminder = reminder
    }
}
